"use strict";

const fs = require("node:fs");
const path = require("node:path");

const obsidianRoot = path.resolve(__dirname, "..");
const clientsRoot = path.resolve(obsidianRoot, "..");
const outputPath = path.join(obsidianRoot, "main.js");

const modules = [
  {
    id: "/protocol/src/generated/protocol-v1.js",
    file: path.join(clientsRoot, "protocol", "src", "generated", "protocol-v1.js"),
  },
  {
    id: "/protocol/src/client.js",
    file: path.join(clientsRoot, "protocol", "src", "client.js"),
  },
  {
    id: "/obsidian/src/plugin-core.js",
    file: path.join(obsidianRoot, "src", "plugin-core.js"),
  },
  {
    id: "/obsidian/src/main.js",
    file: path.join(obsidianRoot, "src", "main.js"),
  },
];

function indent(source) {
  return source
    .replace(/\s*$/u, "")
    .split("\n")
    .map((line) => (line.length === 0 ? "" : `    ${line}`))
    .join("\n");
}

function generateBundle() {
  const moduleFactories = modules.map(({ id, file }) => {
    const source = fs.readFileSync(file, "utf8");
    return [
      `  ${JSON.stringify(id)}: function(module, exports, require) {`,
      indent(source),
      "  }",
    ].join("\n");
  }).join(",\n");

  return [
    "// Generated by clients/obsidian/scripts/build-artifact.js. Do not edit by hand.",
    "\"use strict\";",
    "",
    "const __moduleFactories = Object.freeze({",
    moduleFactories,
    "});",
    "const __moduleCache = Object.create(null);",
    "",
    "function __normalizeModuleId(moduleId) {",
    "  const parts = [];",
    "  for (const part of String(moduleId).split(\"/\")) {",
    "    if (!part || part === \".\") {",
    "      continue;",
    "    }",
    "    if (part === \"..\") {",
    "      parts.pop();",
    "      continue;",
    "    }",
    "    parts.push(part);",
    "  }",
    "  return `/\${parts.join(\"/\")}`;",
    "}",
    "",
    "function __moduleDirname(moduleId) {",
    "  const index = moduleId.lastIndexOf(\"/\");",
    "  return index <= 0 ? \"/\" : moduleId.slice(0, index);",
    "}",
    "",
    "function __resolveBundledModule(parentId, specifier) {",
    "  if (!specifier.startsWith(\".\")) {",
    "    return specifier;",
    "  }",
    "  const baseId = __normalizeModuleId(`\${__moduleDirname(parentId)}/\${specifier}`);",
    "  for (const candidate of [baseId, `\${baseId}.js`, `\${baseId}/index.js`]) {",
    "    if (__moduleFactories[candidate]) {",
    "      return candidate;",
    "    }",
    "  }",
    "  throw new Error(`Cannot find bundled module \${specifier} from \${parentId}`);",
    "}",
    "",
    "function __loadModule(moduleId) {",
    "  if (!__moduleFactories[moduleId]) {",
    "    return require(moduleId);",
    "  }",
    "  if (__moduleCache[moduleId]) {",
    "    return __moduleCache[moduleId].exports;",
    "  }",
    "",
    "  const module = { exports: {} };",
    "  __moduleCache[moduleId] = module;",
    "  __moduleFactories[moduleId](",
    "    module,",
    "    module.exports,",
    "    (specifier) => __loadModule(__resolveBundledModule(moduleId, specifier)),",
    "  );",
    "  return module.exports;",
    "}",
    "",
    "module.exports = __loadModule(\"/obsidian/src/main.js\");",
    "",
  ].join("\n");
}

function main() {
  const bundle = generateBundle();
  if (process.argv.includes("--check")) {
    const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : null;
    if (existing !== bundle) {
      console.error(`${path.relative(process.cwd(), outputPath)} is stale; run node ${path.relative(process.cwd(), __filename)}`);
      process.exitCode = 1;
    }
    return;
  }
  fs.writeFileSync(outputPath, bundle);
}

main();
